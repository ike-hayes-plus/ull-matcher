LAB_HTTP_PORT_NODE_A="${LAB_HTTP_PORT_NODE_A:-8080}"
LAB_HTTP_PORT_NODE_B="${LAB_HTTP_PORT_NODE_B:-8081}"
LAB_HTTP_PORT_NODE_C="${LAB_HTTP_PORT_NODE_C:-8082}"
