LAB_GRPC_PORT_NODE_A="${LAB_GRPC_PORT_NODE_A:-9190}"
LAB_GRPC_PORT_NODE_B="${LAB_GRPC_PORT_NODE_B:-9191}"
LAB_GRPC_PORT_NODE_C="${LAB_GRPC_PORT_NODE_C:-9192}"
