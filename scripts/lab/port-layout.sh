lab_http_url_for_node() {
  case "$1" in
    node-a) echo "http://127.0.0.1:${LAB_HTTP_PORT_NODE_A}" ;;
    node-b) echo "http://127.0.0.1:${LAB_HTTP_PORT_NODE_B}" ;;
    node-c) echo "http://127.0.0.1:${LAB_HTTP_PORT_NODE_C}" ;;
    node-d) echo "http://127.0.0.1:${LAB_HTTP_PORT_NODE_D}" ;;
    *) return 1 ;;
  esac
}
